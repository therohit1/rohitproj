import math
#Number-theoretic and representation functions :-
print(math.ceil(2.9))                            # returns smallest integer greter than equal to x -> 3
print(math.factorial(5))                         # returns factorial of x ->120
print(math.floor(2.9))                           # returns largest integer less than or equal to x -> 2
print(round(2.7))                                # round to the nearest value greater than itself ie. 3
print(abs(-2.9))                                 # returns the absolute value of x -> 2.9
print(math.remainder(11,2))                      #-1.0
print(math.sqrt(4))                              #2.0
print(math.copysign(1.0,-0.0))                   # returns  a float with magnitude (absolute value) of x but the sign of y. -> -1.

 